//
// MapHandler will return an http.HandlerFunc (which also
// implements http.Handler) that will attempt to map any
// paths (keys in the map) to their corresponding URL (values
// that each key in the map points to, in string format).
// If the path is not provided in the map, then the fallback
// http.Handler will be called instead.
func MapHandler(pathsToUrls map[string]string, fallback http.Handler) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {

		// get the path part of the url only
		path := r.URL.Path

		// assert there is a matching path in the map that was passed
		if dest, ok := pathsToUrls[path]; ok {

			// if path is in map, redirect to url
			http.Redirect(w, r, dest, http.StatusFound)

			// return to make sure rest of func doesn't run
			return
		}

		// if there is no mathing path, fallback.
		fallback.ServeHTTP(w, r)
	}
}

